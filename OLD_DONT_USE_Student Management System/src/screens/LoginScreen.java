        char[] password = passwordPasswordField.getPassword();
        // Convert the password to a String if needed
        String passwordString = new String(password);

        if (validateLogin(studentID, passwordString)) {
            // Login successful, open main menu
            openMenuScreen();
        } else {
            // Login failed, display error message
            loginUnsuccessfulLabel.setVisible(true);
        }
